class Reta:
    def __init__(self, _x1, _y1, _x2, _y2, _nome):
        self.x1 = _x1
        self.y1 = _y1
        self.x2 = _x2
        self.y2 = _y2
        self.nome = _nome

    def get_x1(self):
        return self.x1

    def get_y1(self):
        return self.y1

    def get_x2(self):
        return self.x2

    def get_y2(self):
        return self.y2

    def get_nome(self):
        return self.nome

